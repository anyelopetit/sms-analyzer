/**
 * Returns true when every character in the string belongs to the GSM base set.
 *
 * If a single character is outside this set, the entire message must fall back
 * to either Latin-1 or UCS2 depending on whether the rest of the text fits.
 */
export function isGSM7(text) {
  return Array.from(text).every((char) => GSM7_BASIC_CHARSET.has(char));
}

/**
 * Returns true when every character fits inside ISO-8859-1.
 *
 * This is the fallback after GSM. It is strict on code points so the analyzer
 * does not over-accept characters that would require UCS2 in SMS transport.
 */
export function isLatin1(text) {
  return Array.from(text).every((char) => char.codePointAt(0) <= 0xff);
}
